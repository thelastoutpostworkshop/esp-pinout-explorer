function buildWroverKitPin(input: WroverKitPinInput, resolveSourcePinByGpio: BoardSourcePinResolver): SocPin {
  const sourcePin = resolveSourcePinByGpio(input.gpio);

  return makeBoardPin({
    id: `esp-wrover-kit-v4-1-${input.displayNumber.toLowerCase().replace(/[^a-z0-9]+/g, '-')}`,
    number: input.number,
    displayNumber: input.displayNumber,
    name: boardPinName(input.label, input.gpio, preservedLabels),
    label: input.label,
    type: input.type,
    gpio: input.gpio,
    boardHeader: input.boardHeader,
    boardGroup: input.boardGroup,
    position: input.position,
    mainFunctions: input.mainFunctions,
    sourcePin,
    note:
      input.note ??
      `${input.displayNumber} ${input.boardGroup ? `${input.boardGroup} pin` : 'board header pin'}, silkscreen label ${input.label}.`,
    notes: input.notes,
    warnings: input.warnings,
    baseKeywords: commonBoardKeywords,
    keywords: input.keywords,
  });
}

