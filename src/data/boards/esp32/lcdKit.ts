function buildCoreConnectorPin(
  header: 'J1' | 'J2',
  input: LcdKitPinInput,
  resolveSourcePinByGpio: BoardSourcePinResolver,
): SocPin {
  const sourcePin = resolveSourcePinByGpio(input.gpio);
  const displayNumber = `${header}-${input.number}`;

  return makeBoardPin({
    id: `esp32-lcdkit-${header.toLowerCase()}-${input.number}`,
    number: input.number,
    displayNumber,
    name: boardPinName(input.label, input.gpio, ['TXD0', 'RXD0']),
    label: input.label,
    type: input.type,
    gpio: input.gpio,
    boardHeader: header,
    boardGroup: 'ESP32 DevKitC connection module',
    position: { side: header === 'J1' ? 'left' : 'right', order: input.number },
    mainFunctions: input.mainFunctions,
    sourcePin,
    note: `${displayNumber} ESP32 DevKitC connection module pin, silkscreen label ${input.label}.`,
    notes: input.notes,
    warnings: input.warnings,
    baseKeywords: ['board', 'carrier pcb', 'devkitc', 'esp32-devkitc-v4', 'esp32-lcdkit', 'hmi', 'lcdkit', 'module'],
    keywords: input.keywords,
  });
}

