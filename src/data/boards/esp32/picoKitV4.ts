interface PicoKitHeaderPinInput {
  header: 'J2' | 'J3';
  number: number;
  label: string;
  type: PinType;
  gpio?: number;
  mainFunctions: string[];
  notes?: string[];
  warnings?: PinWarning[];
  keywords?: string[];
  flashPad?: boolean;
}

function buildPicoKitV4Pin(input: PicoKitHeaderPinInput, resolveSourcePinByGpio: BoardSourcePinResolver): SocPin {
  const sourcePin = resolveSourcePinByGpio(input.gpio);
  const displayNumber = `${input.header}-${input.number}`;
  const isFlashPad = Boolean(input.flashPad);

  return makeBoardPin({
    id: `esp32-pico-kit-v4-${input.header.toLowerCase()}-${input.number}`,
    number: input.number,
    displayNumber,
    name: boardPinName(input.label, input.gpio, ['TXD0', 'RXD0', 'FSVP', 'FSVN', 'FSD1', 'FSD3', 'FCLK', 'FCS', 'FSD0', 'FSD2']),
    label: input.label,
    type: input.type,
    gpio: input.gpio,
    boardHeader: input.header,
    position: { side: input.header === 'J2' ? 'left' : 'right', order: input.number },
    mainFunctions: input.mainFunctions,
    sourcePin,
    note: `${displayNumber} ${isFlashPad ? 'board pad' : 'board header pin'}, silkscreen label ${input.label}.${isFlashPad ? ' The stock board does not populate a header here.' : ''}`,
    notes: input.notes,
    warnings: uniqueValues([...(input.warnings ?? []), ...(isFlashPad ? ['flash' as PinWarning, 'onboard' as PinWarning] : [])]),
    baseKeywords: [
      'board',
      'devkit',
      'esp32-pico-kit',
      'esp32-pico-kit-v4',
      'esp32-pico-kit-v4.1',
      'header',
      'male headers',
      'female headers',
      'micro-usb',
      'module',
      'pico',
      'pico-d4',
      'pico-kit',
      'pico-kit-v4',
      'pico-kit-v4.1',
      'usb-to-uart',
    ],
    keywords: input.keywords,
  });
}

