function buildDevKitSPin(input: DevKitSPinInput, resolveSourcePinByGpio: BoardSourcePinResolver): SocPin {
  const sourcePin = input.gpio !== undefined ? resolveSourcePinByGpio(input.gpio) : undefined;
  const displayNumber = `${input.header}-${input.number}`;
  const officialLocationLabel = `${input.location}${input.number}`;
  const headerSlug = input.header.toLowerCase().replace(/[^a-z0-9]+/g, '-');

  return makeBoardPin({
    id: `esp8266-devkits-${headerSlug}-${input.number}`,
    number: input.number,
    displayNumber,
    label: input.label,
    type: input.type,
    gpio: input.gpio,
    boardHeader: input.header,
    position: { side: input.header === 'J1' ? 'left' : 'right', order: input.number },
    mainFunctions: input.mainFunctions,
    sourcePin,
    note: `${displayNumber} board header pin (official table label ${officialLocationLabel}), silkscreen label ${input.label}.`,
    notes: input.notes,
    warnings: input.warnings,
    baseKeywords: [
      'board',
      'devkit',
      'devkits',
      'esp8266',
      'esp8266-devkit-s',
      'esp8266-devkits',
      'legacy',
      'eol',
      'header',
      'headers',
      'carrier pcb',
      'flashing board',
      'female headers',
      'spring pins',
      'micro usb',
      'module',
      'module socket',
      'usb-to-uart',
      'wroom',
      'wroom-02',
      'wroom-02d',
      'wroom-02u',
    ],
    keywords: input.keywords,
  });
}

