interface DevKitM1BoardPinInput {
  header: 'J1' | 'J3';
  number: number;
  label: string;
  type: PinType;
  gpio?: number;
  mainFunctions: string[];
  notes?: string[];
  warnings?: PinWarning[];
  keywords?: string[];
}

function warnings(...values: PinWarning[]): PinWarning[] {
  return values;
}

function buildDevKitM1BoardPin(input: DevKitM1BoardPinInput, resolveSourcePinByGpio: SourcePinResolver): SocPin {
  const sourcePin = resolveSourcePinByGpio(input.gpio);
  const displayNumber = `${input.header}-${input.number}`;

  return makeBoardPin({
    id: `esp32s3-devkitm1-${input.header.toLowerCase()}-${input.number}`,
    number: input.number,
    displayNumber,
    label: input.label,
    type: input.type,
    gpio: input.gpio,
    boardHeader: input.header,
    position: { side: input.header === 'J1' ? 'left' : 'right', order: input.number },
    mainFunctions: input.mainFunctions,
    sourcePin,
    note: `${displayNumber} board header pin, silkscreen label ${input.label}.`,
    notes: input.notes,
    warnings: input.warnings,
    baseKeywords: [
      'board',
      'devkit',
      'devkitm',
      'devkitm-1',
      'module',
      'mini',
      'mini-1',
      'mini-1u',
      'header',
    ],
    keywords: input.keywords,
  });
}

